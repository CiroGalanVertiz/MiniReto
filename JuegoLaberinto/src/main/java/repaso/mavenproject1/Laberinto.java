    public void moverJugador(char direccion) {
        int nuevaX = jugadorX;
        int nuevaY = jugadorY;

        switch (direccion) {
            case 'W'->
                nuevaX--;
            case 'S'->
                nuevaX++;
            case 'A'->
                nuevaY--;
            case 'D'->
                nuevaY++;
            default->
                System.out.println("Movimiento inválido.");
        }
        if(nuevaX!=jugadorX){
            if (movimientoValido(nuevaX, nuevaY,"_")) {
            mapa[jugadorX][jugadorY]=mapaAux[jugadorX][jugadorY];
            jugadorX = nuevaX;
            jugadorY = nuevaY;
            mapa[jugadorX][jugadorY] = "J";
        }
        }
        else if (movimientoValido(nuevaX, nuevaY,"|")) {
            mapa[jugadorX][jugadorY]=mapaAux[jugadorX][jugadorY];
            jugadorX = nuevaX;
            jugadorY = nuevaY;
            mapa[jugadorX][jugadorY] = "J";
        }
        else{
            System.out.println("movimiento invalido");
        }
        

        
        
    }

